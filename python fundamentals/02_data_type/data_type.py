"""
Python Data Types Example

This script demonstrates the most common data types in Python.
"""

# Integer -2;-1;0;1;2;3;.......
age = 20

# Float
height = 1.75
# float is  real number
# String
name = "Zafar"
# string is different type of character

# Boolean
is_student = True
# logical valuessssss
# List
languages = ["Python", "Java", "C++"]

# Tuple
coordinates = (10, 20)

# Set
unique_numbers = {1, 2, 3, 4}

# Dictionary
person = {
    "name": "Zafar",
    "age": 20,
    "city": "Tashkent"
}

print("Integer:", age)
print("Float:", height)
print("String:", name)
print("Boolean:", is_student)
print("List:", languages)
print("Tuple:", coordinates)
print("Set:", unique_numbers)
print("Dictionary:", person)